export const convertVotesToText = (number, decimals, recursiveCall) => {
  const noOfLakhs = number / 100000;
  let displayStr = '';
  let isPlural = '';

  if (number >= 10000000000) {
    return number;
  }
  // Rounds off digits to decimalPoints decimal places
  if (noOfLakhs >= 0.01 && noOfLakhs < 1) {
    const noOfThousands = noOfLakhs * 100;
    const thousand = Math.round(noOfThousands);
    isPlural = thousand > 1 && !recursiveCall;
    displayStr = isPlural ? `${thousand} k` : `${thousand} k`;
  } else if (noOfLakhs >= 1 && noOfLakhs < 100) {
    const lakhs = Math.round(noOfLakhs);
    isPlural = lakhs > 1 && !recursiveCall;
    displayStr = isPlural ? `${lakhs} Lacs` : `${lakhs} Lac`;
  } else if (noOfLakhs >= 100) {
    const crores = Math.round(noOfLakhs / 100);
    const crorePrefix = crores >= 100000 ? convertCurrency(crores, decimals, true) : crores;
    isPlural = crores > 1 && !recursiveCall;
    displayStr = isPlural ? `${crorePrefix} Cr` : `${crorePrefix} Cr`;
  } else {
    displayStr = Math.round(+number);
  }

  return displayStr;
};



